    private void btnLuu_InActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuu_InActionPerformed
        // TODO add your handling code here:
        HoaDon_Process hd = new HoaDon_Process();
        tblModel_CTHD = (DefaultTableModel) tblCTHD_TT.getModel();
        try {
            Date nghd = new SimpleDateFormat("yyyy-MM-dd").parse(txtNgayHD.getText());
            java.util.Date utilStarDate1 = nghd;
            java.sql.Date ng_hd = new java.sql.Date(utilStarDate1.getTime());

            int kq = hd.InsertHD(mahd, ng_hd, makh, manv);
            if (kq != 0) {
                for (int i = 0; i < tblModel_CTHD.getRowCount(); i++) {
                    String ma_sp = tblModel_CTHD.getValueAt(i, 0).toString();
                    int sl = (int) tblModel_CTHD.getValueAt(i, 3);
                    int kqi = hd.InsertCTHD(mahd, ma_sp, manv, sl);
                    if (kqi == 0) {
                        JOptionPane.showMessageDialog(this, "Insert CTHD chứa " + ma_sp + " không thành công", "THÔNG BÁO",
                                JOptionPane.OK_OPTION);
                    }
                }
            } else {
                JOptionPane.showMessageDialog(this, "Insert hóa đơn không thành công", "THÔNG BÁO",
                        JOptionPane.OK_OPTION);
            }

        } catch (ParseException ex) {
            Logger.getLogger(ThanhToanHD.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_btnLuu_InActionPerformed

