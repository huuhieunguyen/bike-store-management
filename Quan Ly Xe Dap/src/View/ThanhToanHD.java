    public ThanhToanHD(List<Vector> x, String ma_nv, String ma_kh, String nghd, long tong) {
        initComponents();
        String tieuDe[] = {"MASP", "TÊN SP", "ĐƠN GIÁ", "SL", "THÀNH TIỀN"};
        tblModel_CTHD.setColumnIdentifiers(tieuDe);
        for (int i = 0; i < x.size(); i++) {
            Vector row = x.get(i);
            tblModel_CTHD.addRow(row);
        }
        tblCTHD_TT.setModel(tblModel_CTHD);
        JTableHeader THeader = tblCTHD_TT.getTableHeader();
        THeader.setFont(new Font("Segoe UI", Font.BOLD, 12));
        ((DefaultTableCellRenderer) THeader.getDefaultRenderer()).setHorizontalAlignment(JLabel.CENTER);

        KhachHang kh = new KhachHang();
        Menu m = new Menu();
        manv = ma_nv;
        txtTenNV.setText(m.getTennv(manv));

        txtDiaChi.setText(m.getDiaChiCH(m.getMadv(manv)));
        txtsdtCH.setText(m.getSDT_CH(m.getMadv(manv)));

        makh = ma_kh;
        txtTenKH.setText(kh.getTenKH(makh));

        ngayhd = nghd;
        txtNgayHD.setText(ngayhd);

        HoaDon_Process hd = new HoaDon_Process();
        mahd = hd.getMaHD();
        txtMaHD.setText(mahd);

        txtTong.setValue(tong);
        long tong_giamgia = (long) (tong * hd.getPhanTramGiamGia(tong));
        txtTongGiamGia.setValue(tong_giamgia);

        txtTongThanhToan.setValue(tong - tong_giamgia);

        lbNoiDungKM.setText("(" + hd.getNoiDungKM(tong) + ")");
    }

